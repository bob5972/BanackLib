package net.banack.geometry;

public final class DDimension
{ 
	public double width, height;
	
	
	public DDimension(double width, double height)
	{
		this.width = width;
		this.height = height;
	}
	
	
	public double getWidth()
	{
		return width;
	}
	
	
	public double getHeight()
	{
		return height;
	}	
}

package net.banack.util;

public class EmptyPQException extends EmptyDataStructureException
{

	public EmptyPQException()
	{
	}

	public EmptyPQException(String arg0)
	{
		super(arg0);
	}

	public EmptyPQException(Throwable arg0)
	{
		super(arg0);
	}

	public EmptyPQException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

}

package net.banack.util;

public class EmptyDataStructureException extends RuntimeException
{

	public EmptyDataStructureException()
	{

	}

	public EmptyDataStructureException(String arg0)
	{
		super(arg0);
	}

	public EmptyDataStructureException(Throwable arg0)
	{
		super(arg0);
	}

	public EmptyDataStructureException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

}
